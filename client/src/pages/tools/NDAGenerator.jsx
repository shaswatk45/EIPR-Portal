import { useState } from 'react';
import { DocumentDuplicateIcon, ClipboardDocumentCheckIcon, PrinterIcon } from '@heroicons/react/24/outline';

const DOC_TYPES = [
  { id: 'nda', label: 'Non-Disclosure Agreement (NDA)', icon: '🔒' },
  { id: 'ip_assignment', label: 'IP Assignment Agreement', icon: '📋' },
  { id: 'co_inventor', label: 'Co-Inventor Declaration', icon: '🤝' },
];

const DURATIONS = ['1 year', '2 years', '3 years', '5 years', 'Indefinitely'];

const generateNDA = (form) => {
  const today = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
  return `NON-DISCLOSURE AGREEMENT

This Non-Disclosure Agreement ("Agreement") is entered into as of ${today}
("Effective Date") by and between:

DISCLOSING PARTY:
  ${form.disclosing}
  ("Disclosing Party")

RECEIVING PARTY:
  ${form.receiving}
  ("Receiving Party")

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 1 — PURPOSE

The Disclosing Party intends to disclose certain confidential information relating to:

  "${form.project}"

to the Receiving Party for the purpose of evaluating or assisting with the above-described project or collaboration (the "Purpose").

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 2 — DEFINITION OF CONFIDENTIAL INFORMATION

"Confidential Information" means any data or information disclosed by the Disclosing Party to the Receiving Party, whether orally or in writing, that is designated as confidential or that reasonably should be understood to be confidential, including but not limited to:

  (a) Technical data, trade secrets, know-how, research;
  (b) Product plans, designs, drawings, engineering, and source code;
  (c) Marketing strategies, business plans, financial projections;
  (d) Any information related to the project described in Article 1.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 3 — OBLIGATIONS OF RECEIVING PARTY

The Receiving Party agrees to:

  (a) Hold all Confidential Information in strict confidence;
  (b) Not disclose, transfer, or release the Confidential Information to any third party without prior written consent of the Disclosing Party;
  (c) Use the Confidential Information solely for the Purpose defined herein;
  (d) Protect the Confidential Information with at least the same degree of care used to protect its own confidential information, but in no event less than reasonable care;
  (e) Promptly notify the Disclosing Party upon discovery of any unauthorized use or disclosure of Confidential Information.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 4 — EXCLUSIONS

The obligations of confidentiality shall not apply to information that:

  (a) Is or becomes publicly known through no breach of this Agreement;
  (b) Was rightfully in the Receiving Party's possession prior to disclosure;
  (c) Is independently developed by the Receiving Party without use of Confidential Information;
  (d) Is required to be disclosed by applicable law or court order.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 5 — TERM

This Agreement shall remain in effect for a period of ${form.duration} from the Effective Date, unless terminated earlier by mutual written consent.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 6 — NO LICENSE

Nothing in this Agreement grants the Receiving Party any intellectual property rights, licenses, or ownership in the Confidential Information or any related patents, copyrights, or trademarks.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

ARTICLE 7 — GOVERNING LAW & REMEDIES

This Agreement shall be governed by the laws of India / the applicable jurisdiction. The Disclosing Party shall be entitled to seek injunctive or other equitable relief in addition to any other remedies available at law.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

SIGNATURES

AGREED AND ACCEPTED by the parties hereto:

DISCLOSING PARTY:

  Name: ${form.disclosing}
  Signature: _______________________________
  Date: ___________________________________


RECEIVING PARTY:

  Name: ${form.receiving}
  Signature: _______________________________
  Date: ___________________________________

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

DISCLAIMER: This is a template document for educational purposes only. It does NOT constitute legal advice. Have this agreement reviewed by a qualified attorney before use.

Generated by IPR Awareness Portal | ${new Date().toUTCString()}`;
};

