const NDAGenerator = () => {
  const [form, setForm] = useState({ disclosing: '', receiving: '', project: '', type: 'nda', duration: '2 years' });
  const [doc, setDoc] = useState('');
  const [copied, setCopied] = useState(false);

  const handleChange = (e) => setForm(f => ({ ...f, [e.target.name]: e.target.value }));

  const isValid = form.disclosing && form.receiving && form.project && form.type;

  const handleGenerate = () => {
    if (!isValid) return;
    if (form.type === 'nda') setDoc(generateNDA(form));
    else if (form.type === 'ip_assignment') setDoc(generateIPAssignment(form));
    else setDoc(generateCoInventor(form));
  };

  const handleCopy = () => {
    navigator.clipboard.writeText(doc);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="max-w-5xl mx-auto px-4 py-8 animate-fade-in">
      {/* Header */}
      <div className="text-center mb-10">
        <div className="w-16 h-16 rounded-2xl bg-gradient-to-br from-primary-500 to-secondary-500 flex items-center justify-center mx-auto mb-4 shadow-[0_0_30px_rgba(255,183,0,0.3)]">
          <DocumentDuplicateIcon className="h-9 w-9 text-white" />
        </div>
        <h1 className="page-title">Legal Document Generator</h1>
        <p className="text-slate-400 max-w-xl mx-auto">
          Generate NDA, IP Assignment Agreements, and Co-Inventor Declarations with your details pre-filled — ready to print and sign.
        </p>
      </div>

      <div className="grid lg:grid-cols-2 gap-8">
        {/* Form */}
        <div className="glass-card p-6 space-y-5">
          <h2 className="text-sm font-semibold text-slate-400 uppercase tracking-wider border-b border-white/5 pb-3">Document Details</h2>

          {/* Document Type */}
          <div>
            <label className="label">Document Type *</label>
            <div className="space-y-2">
              {DOC_TYPES.map(dt => (
                <label key={dt.id} className={`flex items-center gap-3 p-3 rounded-xl border cursor-pointer transition-all ${
                  form.type === dt.id
                    ? 'border-primary-500/40 bg-primary-500/10'
                    : 'border-white/5 bg-white/[0.02] hover:border-white/10'
                }`}>
                  <input type="radio" name="type" value={dt.id} checked={form.type === dt.id} onChange={handleChange} className="sr-only" />
                  <span className="text-xl">{dt.icon}</span>
                  <span className="text-sm font-medium text-white">{dt.label}</span>
                </label>
              ))}
            </div>
          </div>

          <div>
            <label className="label">
              {form.type === 'co_inventor' ? 'Inventor 1 (You)' : 'Disclosing Party'} *
            </label>
            <input name="disclosing" value={form.disclosing} onChange={handleChange} className="input"
              placeholder={form.type === 'ip_assignment' ? 'Inventor full name' : 'Your name or organization'} />
          </div>

          <div>
            <label className="label">
              {form.type === 'co_inventor' ? 'Inventor 2 (Co-inventor)' : form.type === 'ip_assignment' ? 'Assignee (Organization)' : 'Receiving Party'} *
            </label>
            <input name="receiving" value={form.receiving} onChange={handleChange} className="input"
              placeholder={form.type === 'ip_assignment' ? 'Organization or company receiving IP' : 'Collaborator or entity name'} />
          </div>

          <div>
            <label className="label">Project / Confidential Information *</label>
            <textarea name="project" value={form.project} onChange={handleChange}
              className="input min-h-[80px] resize-none"
              placeholder="Briefly describe the project or confidential information…" />
          </div>

          {form.type === 'nda' && (
            <div>
              <label className="label">Duration of Confidentiality</label>
              <select name="duration" value={form.duration} onChange={handleChange} className="input">
                {DURATIONS.map(d => <option key={d} value={d}>{d}</option>)}
              </select>
            </div>
          )}

          <button onClick={handleGenerate} disabled={!isValid} className="btn-primary w-full flex items-center justify-center gap-2 disabled:opacity-50">
            <DocumentDuplicateIcon className="h-5 w-5" />
            Generate Document
          </button>
        </div>

        {/* Document Output */}
        <div>
          {doc ? (
            <div className="space-y-4">
              <div className="glass-card border border-primary-500/20 p-4 relative">
                <div className="absolute top-0 left-0 right-0 h-0.5 bg-gradient-to-r from-primary-500 to-secondary-500 rounded-t-2xl" />
                <div className="flex items-center justify-between mb-3">
                  <h3 className="text-sm font-semibold text-primary-300 flex items-center gap-2">
                    <DocumentDuplicateIcon className="h-4 w-4" />
                    {DOC_TYPES.find(d => d.id === form.type)?.label}
                  </h3>
                  <span className="text-xs text-slate-600 font-mono">DRAFT • REVIEW REQUIRED</span>
                </div>
                <pre className="text-xs text-slate-300 whitespace-pre-wrap font-mono leading-relaxed max-h-[580px] overflow-y-auto bg-black/30 rounded-lg p-4 border border-white/5">
                  {doc}
                </pre>
              </div>
              <div className="flex gap-3">
                <button onClick={handleCopy} className="btn-secondary flex-1 flex items-center justify-center gap-2 text-sm">
                  <ClipboardDocumentCheckIcon className="h-4 w-4" />
                  {copied ? '✅ Copied!' : 'Copy Text'}
                </button>
                <button onClick={() => window.print()} className="btn-primary flex-1 flex items-center justify-center gap-2 text-sm">
                  <PrinterIcon className="h-4 w-4" />
                  Print / Save PDF
                </button>
              </div>
              <p className="text-xs text-slate-600 text-center">⚠️ This is a template draft. Have it reviewed by a qualified attorney before signing.</p>
            </div>
          ) : (
            <div className="glass-card border border-dashed border-white/10 p-10 text-center h-full flex flex-col items-center justify-center gap-4">
              <DocumentDuplicateIcon className="h-12 w-12 text-slate-700" />
              <p className="text-slate-500 text-sm">Your legal document will appear here once you fill in the details and click Generate.</p>
              <div className="text-xs text-slate-600 space-y-1">
                <p>✦ NDA Templates</p>
                <p>✦ IP Assignment Agreements</p>
                <p>✦ Co-Inventor Declarations</p>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
};

export default NDAGenerator;
